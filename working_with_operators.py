"""
Этот модуль содержит программу для работы с основными операторами языка Python.

Программа запрашивает у пользователя диапазон чисел, находит все чётные числа
в указанном диапазоне и вычисляет их сумму.
"""

# Функция для получения чётных чисел в заданном диапазоне
def get_even_numbers(start, end):
    """
    Возвращает список чётных чисел в заданном диапазоне.
    
    Параметры:
    start (int): Начало диапазона.
    end (int): Конец диапазона.
    
    Возвращает:
    list: Список чётных чисел от start до end.
    """
    even_numbers = []
    for number in range(start, end + 1):
        if number % 2 == 0:
            even_numbers.append(number)
    return even_numbers

# Функция для вычисления суммы списка чисел
def sum_of_numbers(numbers):
    """
    Вычисляет сумму чисел из списка.
    
    Параметры:
    numbers (list): Список чисел для суммирования.
    
    Возвращает:
    int: Сумма чисел в списке.
    """
    total_sum = 0
    for num in numbers:
        total_sum += num
    return total_sum

# Основная функция программы
def main():
    """
    Основная логика программы.
    
    Запрашивает у пользователя диапазон чисел, находит чётные числа,
    вычисляет их сумму и выводит результаты.
    """
    start = int(input("Введите начало диапазона: "))
    end = int(input("Введите конец диапазона: "))

    if start > end:
        print("Ошибка: начало диапазона больше конца!")
        return

    even_numbers = get_even_numbers(start, end)
    print("Чётные числа в диапазоне от", start, "до", end, ":", even_numbers)

    even_sum = sum_of_numbers(even_numbers)
    print("Сумма чётных чисел:", even_sum)

    # Ожидание, чтобы окно программы не закрывалось сразу
    input("Нажмите Enter для завершения программы...")

# Запуск основной функции программы
if __name__ == "__main__":
    main()
